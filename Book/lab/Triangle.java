public class Triangle 
{

    private int x;
    private int y;
    private int z;
    
    public void setXYZ(int x,int y,int z)
    {
        this.x=x;
        this.y=y;
        this.z=z;
    }
    
    public int getX()
    {
        return this.x;
    }
    
    public int gety()
    {
        return y;
    }
    
    public int getz()
    {
        return z;
    }
}
